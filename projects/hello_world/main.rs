
// main function is special 
// it's always the first code that runs in every executable rust program.
fn main() {
    // important details to notice
    // 1. rust stuyle is to indent with 4 spaces.
    // 2. println! calls Rust macros.
    // ! means that you are calling macro instead of normal function.
    // macros doesn't follow same rules as function.
    // 3. to pass args as string to fn.
    // 4. we end the line with a semicolon (;),
    
    println!("Hello world!");
}